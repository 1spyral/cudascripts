#pragma once

#include <iostream>

/**
 * @brief Prints the elements of an integer array.
 *
 * This function prints the elements of the given integer array in a 
 * formatted manner. If the array is empty, it prints "{}". Otherwise, 
 * it prints the elements enclosed in curly braces and separated by spaces.
 *
 * @param arr Pointer to the integer array to be printed.
 * @param size The number of elements in the array.
 */
void printArray(int* arr, size_t size);